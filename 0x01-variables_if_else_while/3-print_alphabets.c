#include <stdio.h>
#include <stdlib.h>
/**
 * main -main function to print the alphabet lowercase and uppercase
 * Return: Always 0
 *
*/
int main(void)
{
	int n = 'a';
	int m = 'A';

	for (n = 'a'; n <= 'z'; n++)
	{
		putchar(n);
	}
	for (m = 'A'; m <= 'Z'; m++)
	{
		putchar(m);
	}
	putchar('\n');
	return (0);
}
